FVector UMarchingCubes::Rot(FVector coord, const FMatrix& mat) {
    // 3x3 transform, ignore translation.
    return FVector(
        FVector::DotProduct(mat.GetScaledAxis(EAxis::X), coord),
        FVector::DotProduct(mat.GetScaledAxis(EAxis::Y), coord),
        FVector::DotProduct(mat.GetScaledAxis(EAxis::Z), coord)
    );
}

